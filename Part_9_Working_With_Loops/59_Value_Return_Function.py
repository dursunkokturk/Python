# Ucgenin Uzunluklarinin Girilecegi Degerler Icin Degiskenleri Tanimliyoruz
def dikUcgenAlani(a,b):
    
    # Fonksiyon Icinde Yapilacak Islem Belirtiliyor
    return a*b/2

# Fonksiyon Icindeki Islemin Yapilmasi Icin 
# Fonksiyon Adini Yazdiktan SonraParametrelere
# Parametrelere Degerler Gonderiyoruz
# Fonksiyon Icindeki Islemin Sonucunu Degiskene Atama Yapiyoruz
triangelAreaCalculation = dikUcgenAlani(2,3)

